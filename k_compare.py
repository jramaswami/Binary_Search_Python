import heapq


class Solution:

    def solve(self, a, b, k):
        if not b:
            return len(a)

        if not a:
            return 0

        if k == 0:
            return len(a)

        x = min(heapq.nlargest(k, b))
        return sum(n < x for n in a)


def test_1():
    a = [5, -3, 99, 10]
    b = [32, 5, 29, 7, 13]
    k = 3
    expected = 3
    assert Solution().solve(a, b, k) == expected


