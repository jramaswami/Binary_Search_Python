"""
binarysearch.com :: Split List to Minimize Largest Sum
jramaswami
"""


class Solution:

    def solve(self, nums, k):

        def count_subarrays(target):
            curr_sum = 0
            subarrays = 0
            for n in nums:
                curr_sum += n
                if curr_sum > target:
                    subarrays += 1
                    curr_sum = n
            # Current subarray.
            subarrays += 1
            return subarrays


        # Binary search for the answer.
        lo = max(nums)
        hi = sum(nums)
        soln = hi
        while lo <= hi:
            mid = lo + ((hi - lo) // 2)
            if count_subarrays(mid) <= k:
                soln = min(soln, mid)
                hi = mid - 1
            else:
                lo = mid + 1
        return soln


